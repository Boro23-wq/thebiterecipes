// SEED SCRIPT WITH IMAGE UPLOAD
// ============================================
async function seed() {
  console.log("🌱 Seeding recipe database with images...\n");

  let successCount = 0;
  let imageFailCount = 0;

  for (let i = 0; i < recipes.length; i++) {
    const recipe = recipes[i];
    console.log(`[${i + 1}/${recipes.length}] ${recipe.title}`);

    // Step 1: Fetch image from Pexels
    let imageUrl: string | null = null;
    const pexelsResult = await fetchPexelsImage(recipe.imageSearch);

    if (pexelsResult) {
      // Step 2: Upload to UploadThing
      const slug = recipe.title
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, "-")
        .replace(/(^-|-$)/g, "");
      imageUrl = await uploadToUploadThing(pexelsResult.url, `seed-${slug}`);

      if (imageUrl) {
        console.log(
          `  📸 Image uploaded (credit: ${pexelsResult.photographer})`,
        );
      } else {
        console.log(`  ⚠️  Image upload failed, continuing without image`);
        imageFailCount++;
      }
    } else {
      console.log(`  ⚠️  No image found, continuing without image`);
      imageFailCount++;
    }

    // Step 3: Insert recipe
    const [inserted] = await db
      .insert(seedRecipes)
      .values({
        title: recipe.title,
        description: recipe.description,
        imageUrl,
        servings: recipe.servings,
        prepTime: recipe.prepTime,
        cookTime: recipe.cookTime,
        totalTime: recipe.totalTime,
        difficulty: recipe.difficulty,
        cuisine: recipe.cuisine,
        dietaryTags: recipe.dietaryTags,
        timeCategory: recipe.timeCategory,
        calories: recipe.calories,
        protein: recipe.protein,
        carbs: recipe.carbs,
        fat: recipe.fat,
      })
      .returning({ id: seedRecipes.id });

    // Step 4: Insert ingredients
    if (recipe.ingredients.length > 0) {
      await db.insert(seedRecipeIngredients).values(
        recipe.ingredients.map((ing, idx) => ({
          seedRecipeId: inserted.id,
          ingredient: ing.ingredient,
          amount: ing.amount,
          order: idx + 1,
        })),
      );
    }

    // Step 5: Insert instructions
    if (recipe.instructions.length > 0) {
      await db.insert(seedRecipeInstructions).values(
        recipe.instructions.map((step, idx) => ({
          seedRecipeId: inserted.id,
          step,
          order: idx + 1,
        })),
      );
    }

    console.log(`  ✅ Saved to database`);
    successCount++;

    // Rate limit: wait between Pexels API calls
    await sleep(250);
  }

  console.log(`\n🎉 Done! ${successCount} recipes seeded.`);
  if (imageFailCount > 0) {
    console.log(
      `⚠️  ${imageFailCount} recipes without images (can be added later).`,
    );
  }
  process.exit(0);
}

seed().catch((err) => {
  console.error("❌ Seed failed:", err);
  process.exit(1);
});
