// ============================================
// PEXELS IMAGE FETCHER
// ============================================
async function fetchPexelsImage(
  query: string,
): Promise<{ url: string; photographer: string } | null> {
  try {
    const res = await fetch(
      `https://api.pexels.com/v1/search?query=${encodeURIComponent(query)}&per_page=3&orientation=landscape`,
      { headers: { Authorization: PEXELS_API_KEY } },
    );
    if (!res.ok) {
      console.warn(`  ⚠️  Pexels API error for "${query}": ${res.status}`);
      return null;
    }
    const data = await res.json();
    if (!data.photos?.length) {
      console.warn(`  ⚠️  No Pexels results for "${query}"`);
      return null;
    }
    const photo = data.photos[0];
    return { url: photo.src.large, photographer: photo.photographer };
  } catch (err) {
    console.warn(`  ⚠️  Pexels fetch failed for "${query}":`, err);
    return null;
  }
}

// ============================================
// UPLOAD TO UPLOADTHING
// ============================================
async function uploadToUploadThing(
  imageUrl: string,
  fileName: string,
): Promise<string | null> {
  try {
    const res = await fetch(imageUrl);
    if (!res.ok) return null;
    const blob = await res.blob();
    const file = new File([blob], `${fileName}.jpg`, { type: "image/jpeg" });
    const uploadResponse = await utapi.uploadFiles(file);
    if (uploadResponse.error) {
      console.warn(`  ⚠️  UploadThing error: ${uploadResponse.error.message}`);
      return null;
    }
    return uploadResponse.data.ufsUrl || uploadResponse.data.url;
  } catch (err) {
    console.warn(`  ⚠️  Upload failed for ${fileName}:`, err);
    return null;
  }
}

